void create_font(const char* definition, const int width, int height) {

  char *currentChar = (char*) definition;
  int i, k;
 
  printf("const int CHAR_SET_%dx%d[] = {\n    ", width, height);
  
   for (i = 0; i < NUMBER_OF_CHARACTERS; i++) {
 	
 	  for (k = 0; k < height; k++) {
	 
	 	  int pos; 	  
	 	  char c;
	 	  unsigned char result = 0;
	 	 
	 	  for (pos = 0; pos < width; pos++) {
	  	  c = *currentChar++;
	      if (c == '#') {
	        result = result | 1 << pos;
	      }
	    }
	 	
	    printf("%d", result);  
      printf(", "); 
	    } 
	    printf("\n    ");     
	  }
	  
	  
	
	  printf(" -1 };\n\n");
  
  
}
  
