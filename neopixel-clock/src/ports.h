/*
 * Definition of the ports used by the word clock.
 */

#ifndef __ports_h__
#define __ports_h__

/* on-board LED of Arduino */
#define PORT_LED 13

/* port for button number one */
#define PORT_BUTTON1 17

/* port for button number two */
#define PORT_BUTTON2 18

/* port for button number three */
#define PORT_BUTTON3 19

/** port the Neopixel strip is connected to */
#define PIN_NEOPIXEL 16

/** port the ambient light sensor is connected to */
#define PIN_LIGHTSENSOR 2  // is connected to D2

#endif
